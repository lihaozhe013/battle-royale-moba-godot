#!/usr/bin/env python3
"""Package the Godot project + GDExtension into a release build.

Builds the C++ GDExtension in release mode, then exports the Godot
project using the headless Godot executable, and zips the result.

Usage:
    uv run package.py            Package for current platform
    uv run package.py windows    Package for Windows
    uv run package.py macos      Package for macOS
    uv run package.py all        Package for all supported platforms
"""

import argparse
import os
import platform
import shutil
import subprocess
import sys
import zipfile

PROJECT_DIR = os.path.dirname(os.path.abspath(__file__))
DIST_DIR = os.path.join(PROJECT_DIR, "dist")
ADDON_DIR = os.path.join(PROJECT_DIR, "addons", "topdown_sim")

HOST_PLATFORM = {
    "win32": "windows",
    "darwin": "macos",
}.get(sys.platform)


def _find_godot():
    godot = os.environ.get("GODOT_EXE")
    if godot and os.path.isfile(godot):
        return godot
    godot = shutil.which("godot")
    if godot:
        return godot
    candidates = [
        "/c/godot/godot.exe",
        "C:/godot/godot.exe",
        "/usr/local/bin/godot",
        "/Applications/Godot.app/Contents/MacOS/Godot",
    ]
    for c in candidates:
        if os.path.isfile(c):
            return c
    print("Error: Godot executable not found. Set GODOT_EXE env var or add it to PATH.")
    sys.exit(1)


def _build_gdextension():
    """Build the C++ GDExtension in release mode via build.py."""
    print("\n=== Building GDExtension (template_release) ===")
    build_script = os.path.join(PROJECT_DIR, "build.py")
    if not os.path.isfile(build_script):
        print("Error: build.py not found in project root.")
        sys.exit(1)
    result = subprocess.call(
        [sys.executable, build_script, "build", "--target", "template_release"],
        cwd=PROJECT_DIR,
    )
    if result != 0:
        print("Error: GDExtension build failed.")
        sys.exit(1)


